    private void btOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOKActionPerformed

        String ques = taQuestion.getText().trim();
        String ansA = taA.getText().trim();
        String ansB = taB.getText().trim();
        String ansC = taC.getText().trim();
        String ansD = taD.getText().trim();
        String correct = tfCorrect.getText().trim();
        String level = cbLevel.getSelectedItem().toString();

        if (check.checkString(ques) && check.checkString(ansA) && check.checkString(ansB)
                && check.checkString(ansC) && check.checkString(ansD) && check.checkString(correct)) {
            // Add
            if (addOrUpdate) {
                Question que = new Question(tfID.getText().trim(), ques, ansA, ansB, ansC, ansD, correct, level, idSubject);
                if (addOrUpdate(true, que)) {
                    show("Thêm mới thành công!\n", 1);
                }
                // Update
            } else {
                Question que = new Question(tfID.getText().trim(), ques, ansA, ansB, ansC, ansD, correct, level);
                if (addOrUpdate(false, que)) {
                    show("Cập nhật thành công!\n", 1);
                }
            }
            btRefresh.doClick();
        } else {
            show("Phải điền đầy đủ thông tin.", 0);
        }


    }//GEN-LAST:event_btOKActionPerformed

