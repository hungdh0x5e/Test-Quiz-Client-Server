    private void loadColumnData() {
        Vector<String> columnName = questionDAO.getColumnName();
        columnName.add(0, "STT");
        columnName.remove(1);
        columnName.remove(columnName.size() - 1);
        model.setColumnIdentifiers(columnName);
    }

    private void loadRowData(int i) {
        removeRow();
        setEnableInput(false);
        setEnableButton2(false);
        int stt = 0;
        switch (i) {
            //Load data question
            case 0:
                vtQuestion = questionDAO.getRowData(idSubject);
                for (Question a : vtQuestion) {
                    stt++;
                    model.addRow(toObjectData(stt, a));
                }
                break;
            // Load data from box search.
            case 1:
                for (Question a : vtQuesSearch) {
                    stt++;
                    model.addRow(toObjectData(stt, a));
                }
                break;
            default:
                show("Sai chỉ số", 0);
                return;
        }
        tbQuestion.setModel(model);

    }

