    private Object[] toObjectData(int stt, Question ques) {
        Object[] obj = {stt, ques.getQuestion(), ques.getAnswerA(), ques.getAnswerB(),
            ques.getAnswerC(), ques.getAnswerD(), ques.getCorrect(), ques.getLevel()};
        return obj;
    }

    private boolean addOrUpdate(boolean add, Question ques) {
        // Button add
        if (add) {
            if (questionDAO.checkIDQuestion(ques.getID())) {
                show("Thêm mới không thành công!\n<html>Câu hỏi: <b>" + ques.getID()
                        + "</b> đã tồn tại.\nVui lòng thử lại.", 0);
                return false;
            } else {
                questionDAO.addQuestion(ques);
                return true;
            }
            // Button update
        } else {
            if (!questionDAO.checkIDQuestion(ques.getID())) {
                show("Cập nhật không thành công!\n<html>Câu hỏi: <b>" + ques.getID()
                        + "</b> không có trong hệ thống.\nVui lòng thử lại.", 0);
                return false;
            } else {
                questionDAO.updateQuestion(ques);
                return true;
            }
        }
    }

