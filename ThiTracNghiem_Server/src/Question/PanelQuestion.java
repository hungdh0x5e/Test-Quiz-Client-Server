    private void tfSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfSearchKeyReleased
        if (iSearch != 0) {
            String keyword = tfSearch.getText().trim();
            vtQuesSearch.removeAllElements();
            if (iSearch == 1) {
                for (Question ques : vtQuestion) {
                    if (ques.getID().toLowerCase().contains(keyword.toLowerCase())) {
                        vtQuesSearch.add(ques);
                    }
                }
            } else {
                for (Question ques : vtQuestion) {
                    if (ques.getQuestion().toLowerCase().contains(keyword.toLowerCase())) {
                        vtQuesSearch.add(ques);
                    }
                }
            }
            if (vtQuesSearch.size() != 0) {
                loadRowData(1);
            }
        } else {
            show("Bạn cần chọn chế độ tìm kiếm", 0);
        }
    }//GEN-LAST:event_tfSearchKeyReleased
