    private void countDown() {
        dongho = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (--timeRemain >= 0) {
                    int i = timeRemain / 60;
                    int j = timeRemain - i * 60;
                    lbRemain.setText(((i > 9) ? ("" + i) : ("0" + i)) + " : " + ((j > 9) ? ("" + j) : ("0" + j)));
                } else {
                    JOptionPane.showMessageDialog(null, "Hết giờ. Bài thi đã tự động được gửi!");  
                    btCancel.setText("Quay lại");
                    dongho.stop();
                    showResult();
                }
            }
        });
        dongho.start();
    }

