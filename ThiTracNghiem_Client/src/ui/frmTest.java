    public void loadQuestion() {
        Color color = Color.WHITE;
        String fonts = "Times New Roman";
        int size = 16;
        pnlRoot.setBackground(color);

        for (int i = 0; i < MAX; i++) {
            Question ques = vtQuestion.get(i);

            pnlMain[i] = new JPanel(new BorderLayout());
            pnlMain[i].setSize(490, 150);
            pnlTop[i] = new JPanel(new FlowLayout(0));
            pnlCenter[i] = new JPanel(new GridLayout(2, 2, 5, 10));

            label[2 * i] = new JLabel(html2 + "Câu " + (i + 1) + ": " + ques.getQuestion());
            label[2 * i].setFont(new Font(fonts, 1, size));
            label[2 * i].setForeground(new Color(0, 0, 255));

            label[2 * i + 1] = new JLabel(html + "<b>A.</b> " + ques.getAnswerA());
            label[2 * i + 1].setFont(new Font(fonts, 0, size));
            label[2 * i + 1].setForeground(new Color(0, 0, 0));

            label[2 * i + 2] = new JLabel(html + "<b>B.</b> " + ques.getAnswerB());
            label[2 * i + 2].setFont(new Font(fonts, 0, size));
            label[2 * i + 2].setForeground(new Color(0, 0, 0));

            label[2 * i + 3] = new JLabel(html + "<b>C.</b> " + ques.getAnswerC());
            label[2 * i + 3].setFont(new Font(fonts, 0, size));
            label[2 * i + 3].setForeground(new Color(0, 0, 0));

            label[2 * i + 4] = new JLabel(html + "<b>D.</b> " + ques.getAnswerD());
            label[2 * i + 4].setFont(new Font(fonts, 0, size));
            label[2 * i + 4].setForeground(new Color(0, 0, 0));

            pnlTop[i].add(label[2 * i]);
            pnlCenter[i].add(label[2 * i + 1], 0);
            pnlCenter[i].add(label[2 * i + 2], 1);
            pnlCenter[i].add(label[2 * i + 3], 2);
            pnlCenter[i].add(label[2 * i + 4], 3);

            pnlTop[i].setBackground(color);
            pnlCenter[i].setBackground(color);
            pnlMain[i].setBackground(color);

            pnlMain[i].add(pnlTop[i], BorderLayout.NORTH);
            pnlMain[i].add(pnlCenter[i], BorderLayout.CENTER);
            pnlRoot.add(pnlMain[i]);
        }

    }


